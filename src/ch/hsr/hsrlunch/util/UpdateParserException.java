package ch.hsr.hsrlunch.util;

public class UpdateParserException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UpdateParserException() {
		super();
	}

	public UpdateParserException(String message) {
		super(message);
	}

	public UpdateParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public UpdateParserException(Throwable cause) {
		super(cause);
	}
}
