package ch.hsr.hsrlunch.util;

public class UpdateBadgeException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UpdateBadgeException() {
		super();
	}

	public UpdateBadgeException(String message) {
		super(message);
	}

	public UpdateBadgeException(String message, Throwable cause) {
		super(message, cause);
	}

	public UpdateBadgeException(Throwable cause) {
		super(cause);
	}
}
