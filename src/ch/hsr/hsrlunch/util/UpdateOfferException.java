package ch.hsr.hsrlunch.util;

public class UpdateOfferException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UpdateOfferException() {
		super();
	}

	public UpdateOfferException(String message) {
		super(message);
	}

	public UpdateOfferException(String message, Throwable cause) {
		super(message, cause);
	}

	public UpdateOfferException(Throwable cause) {
		super(cause);
	}
}
